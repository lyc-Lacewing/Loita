using Terraria.ModLoader;

namespace Loita
{
	public class Loita : Mod
	{
        /*
		 * 法杖: Wand, 施法媒介
		 * 槽位: Slot, 法杖的容量
		 * 槽位的填充物: Infusion, 包含各类法术与修饰
		 * 法术: Spell, 包含各类法术
		 * 动态法术: Dynamic Spell, 施放与生效快速且动态的法术, 如火花魔弹
		 * 静态法术: Static Spell, 静态且生效较慢的法术, 如场地魔法
		 * 被动法术: Passive Spell, 无需施法, 自动施放的法术
		 * 法术修饰: Modifier, 法术的修饰, 改变法术的属性
		 * 施法: Cast, 施放法术的动作
		 * 序列: Sequence, 施法前, 法术与修饰会按顺序排好, 依次施放
		 * 延迟: Delay, 两个法术间的延迟
		 * 充能: Recharge, 每轮施法后, 法杖需要一定时间充能, 才可开始下一轮施法
		 * 魔耗: Mana Cost, 施放法术需要消耗的魔力
		 * 随机偏移: Scatter, 法术在施放时随机偏移
		 */
    }
}